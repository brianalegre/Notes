// Ternary Operator is the shorthand for IF statements

// Variables
let num2 = 1;
let num1 = 1;

// Typical IF statement
if (num1 === num2) {
	console.log('Numbers are the same');
} else {
	console.log('Numbers are different');
}

// Displays:
// Numbers are the same

// Tenary Operator
num1 === num2
	? console.log('Numbers are the same')
	: console.log('Numbers are different');

// Displays:
// Numbers are the same

// ? - is your IF
// After the ? is the true section
// : - is your ELSE
// After the : is your false section

// Structure, 3 PARTS separated by ? and :
// CONDITION ? IF TRUE DO THIS : ELSE DO THIS
